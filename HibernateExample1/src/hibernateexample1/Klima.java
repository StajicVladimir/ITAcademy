public class Klima {
    private int id;
    private String model;
    private String proivodjac;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the proivodjac
     */
    public String getProivodjac() {
        return proivodjac;
    }

    /**
     * @param proivodjac the proivodjac to set
     */
    public void setProivodjac(String proivodjac) {
        this.proivodjac = proivodjac;
    }
    
}
